class Calculator:

    @staticmethod
    def soma(a, b):
        return(float(a)+float(b))

    @staticmethod
    def subt(a, b):
        return(float(a)-float(b))

    @staticmethod
    def mult(a, b):
        return(float(a)*float(b))

    @staticmethod
    def div(a, b):
        return(float(a)/float(b))